package Model;

/**
 *
 * @author ASUS
 */
public class Account {
    String username;
    String pass;
    String vaitro;
    String hoten;
    String diachi;
    String sdt;
    String email;

    public Account() {
    }

    public Account(String username, String pass, String vaitro, String hoten, String diachi, String sdt, String email) {
        this.username = username;
        this.pass = pass;
        this.vaitro = vaitro;
        this.hoten = hoten;
        this.diachi = diachi;
        this.sdt = sdt;
        this.email = email;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getVaitro() {
        return vaitro;
    }

    public void setVaitro(String vaitro) {
        this.vaitro = vaitro;
    }

    public String getHoten() {
        return hoten;
    }

    public void setHoten(String hoten) {
        this.hoten = hoten;
    }

    public String getDiachi() {
        return diachi;
    }

    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }

    public String getSdt() {
        return sdt;
    }

    public void setSdt(String sdt) {
        this.sdt = sdt;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
}
